public class BasicTools {
    private String desc;
    private String video;
    private EquipmentsBasicTools Equipments;

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

    public EquipmentsBasicTools getEquipments() {
        return Equipments;
    }

    public void setEquipments(EquipmentsBasicTools equipments) {
        Equipments = equipments;
    }
}
